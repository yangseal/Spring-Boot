package com.cnten.platform.web;

/**
 * 操作结果编码，用于区分不同原因造成 的成功、失败
 * 
 * @author Administrator
 */

public enum ResultCode {
	fail_0x000("0x000"), // 通过操作失败
	
	
	success_0x001("0x001"), // 通用操作成功
	success_0x002("0x002"); // 使用申请该类型已经使用过了,不能再次重复使用		(该试用服务一个账号只能申请一次)
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

	// 定义私有变量
	private String nCode;

	// 构造函数，枚举类型只能为私有
	private ResultCode(String _nCode) {
		this.nCode = _nCode;
	}

	@Override
	public String toString() {
		return String.valueOf(this.nCode);
	}
}
